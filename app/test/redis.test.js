require("dotenv").config();
const app = require("../src/app");

test("#1 Validate redis", async () => {
  const users = [{ userId: 10, secret: 123 }];
  await app.config.redisClient.set("users_secret", JSON.stringify(users));
  const values = JSON.parse(await app.config.redisClient.get("users_secret"));
  expect(values[0].secret).toBe(123);
});
